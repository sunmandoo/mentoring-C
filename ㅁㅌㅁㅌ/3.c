#include <stdio.h>

int main()
{
    int First_Number = 20;
    int Second_Number = 10;

    printf("두 수의 합: %d \n", First_Number + Second_Number);
    printf("두 수의 차: %d \n", First_Number - Second_Number);
    printf("두 수의 곱: %d \n", First_Number * Second_Number);
    printf("두 수의 몫: %d \n", First_Number / Second_Number);

    return 0;
}